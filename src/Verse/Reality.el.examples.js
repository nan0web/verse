import { Παρατηρητής, Δυναμικό, Φαινόμενο, πρα1τικότητα, πρα0τικότητα, θ0ός } from './Reality.el.js'

// 1. Δημιουργία κόσμου
const θεός = new θ0ός()
const αρχικάΔυναμικά = [
	new Δυναμικό('ενέργεια'),
	new Δυναμικό('ύλη'),
	new Δυναμικό('συνείδηση')
]
const κόσμος = θεός.εμπλέκω(αρχικάΔυναμικά)

// 2. Αντικειμενική πραγματικότητα σε διαφορετικές στιγμές
const τώρα = new πρα0τικότητα()
const παρελθόν = new πρα0τικότητα()
const μέλλον = new πρα0τικότητα()

τώρα.εμπλέκω([new Δυναμικό('παρόν')])
παρελθόν.εμπλέκω([new Δυναμικό('μνήμη')])
μέλλον.εμπλέκω([new Δυναμικό('δυνατότητα')])

// 3. Αλληλεπίδραση πραγματικοτήτων
const παρατηρητής1 = new Παρατηρητής('άνθρωπος1')
const παρατηρητής2 = new Παρατηρητής('άνθρωπος2')

const πραγματικότητα1 = new πρα1τικότητα(παρατηρητής1)
const πραγματικότητα2 = new πρα1τικότητα(παρατηρητής2)

const κβαντικόΦαινόμενο = new Φαινόμενο('κβαντικό γεγονός')

// Διαφορετικές παρατηρήσεις από διαφορετικές πραγματικές
const παρατήρηση1 = πραγματικότητα1.παρατηρώ(κβαντικόΦαινόμενο)
const παρατήρηση2 = πραγματικότητα2.παρατηρώ(κβαντικόΦαινόμενο)

console.log('Ο κόσμος δημιουργήθηκε:', κόσμος)
console.log('Χρονική πραγματικότητα:', { τώρα, παρελθόν, μέλλον })
console.log('Διαπραγματικές παρατηρήσεις:', { παρατήρηση1, παρατήρηση2 })
